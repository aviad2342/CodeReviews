    public void AddBug(Bug bug) {
         int i = 0;
         if(bug != null){
             bugs.add(bug);   
          DefaultListModel<String> modle = new DefaultListModel<String>(); 
            for(Bug b : bugs)
            {  
                modle.addElement((++i)+"."+b.getBugsDescription());
            }
            BugList.setModel(modle);
         }  
    }
    
