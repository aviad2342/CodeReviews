    private void formMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseMoved
       int rate= ((StarRater)starRater).getSelection();
       String tip= taSuggestionsAndTips.getText();
       if(rate>0 || tip.length()>0 ||bugs.size()>0||shareCheckBox.isSelected())
       {
           cbFilesToReview.setEnabled(false);
       }
       else
       {
          cbFilesToReview.setEnabled(true); 
       }
    }//GEN-LAST:event_formMouseMoved

