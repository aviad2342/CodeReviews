    private void btnRemoveBugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveBugActionPerformed
        bugs.remove(BugList.getSelectedIndex());
        ((DefaultListModel<String>)BugList.getModel()).remove(BugList.getSelectedIndex());
        if( ((DefaultListModel<String>)BugList.getModel()).isEmpty())
        {
            this.btnRemoveBug.setEnabled(false);
        }
    }//GEN-LAST:event_btnRemoveBugActionPerformed

